def _patch_paper_anti_xray_yaml(content: str) -> str:
    """paper-world-defaults.yml 의 anti-xray 섹션만 enabled=true, engine-mode=2 로 패치.

    YAML 라이브러리 의존성 없이 라인 기반으로 처리. anti-xray: 키를 만나면 그
    들여쓰기 깊이를 기억하고, 같거나 얇은 들여쓰기가 다시 나올 때까지 안에서
    enabled / engine-mode 만 교체. lava-obscures 같이 같은 이름이 다른 위치에
    중복되는 키는 안 건드림 — enabled / engine-mode 는 anti-xray 직속 자식이지
    nested 가 아니라서 indent == section_indent + 2 조건으로 정확히 잡힘.
    """
    out = []
    in_anti_xray = False
    section_indent = -1
    for line in content.splitlines():
        stripped = line.lstrip()
        if not stripped or stripped.startswith("#"):
            out.append(line)
            continue
        indent = len(line) - len(stripped)
        if not in_anti_xray:
            if stripped.startswith("anti-xray:"):
                in_anti_xray = True
                section_indent = indent
            out.append(line)
            continue
        if indent <= section_indent:
            in_anti_xray = False
            out.append(line)
            continue
        if stripped.startswith("enabled:") and indent == section_indent + 2:
            out.append(" " * indent + "enabled: true")
            continue
        if stripped.startswith("engine-mode:") and indent == section_indent + 2:
            out.append(" " * indent + "engine-mode: 2")
            continue
        out.append(line)
    suffix = "\n" if content.endswith("\n") else ""
    return "\n".join(out) + suffix


